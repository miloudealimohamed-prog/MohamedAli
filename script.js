let currentLanguage = localStorage.getItem('selectedLanguage') || 'ar';
function loadTranslations(lang) {
  const t = translations[lang];
  if (!t) return;
  document.querySelectorAll('[data-key]').forEach(el => {
    const key = el.getAttribute('data-key');
    if (t[key]) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') el.placeholder = t[key];
      else el.textContent = t[key];
    }
  });
  document.body.style.direction = lang === 'ar' ? 'rtl' : 'ltr';
  document.body.className = lang;
}
function changeLanguage(lang) {
  currentLanguage = lang;
  localStorage.setItem('selectedLanguage', lang);
  document.querySelectorAll('.lang-btn').forEach(btn => btn.classList.toggle('active', btn.getAttribute('data-lang') === lang));
  loadTranslations(lang);
  showToast(lang === 'ar' ? '✓ تم تغيير اللغة إلى العربية' : lang === 'en' ? '✓ Language changed to English' : '✓ Langue changée en français');
}
